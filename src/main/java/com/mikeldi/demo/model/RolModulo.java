package com.mikeldi.demo.model;

public class RolModulo {
	private Rol rol;
	private String modulo;
	
	public RolModulo(Rol rol, String modulo) {
		super();
		this.rol = rol;
		this.modulo = modulo;
	}
	public Rol getRol() {
		return rol;
	}
	public void setRol(Rol rol) {
		this.rol = rol;
	}
	public String getModulo() {
		return modulo;
	}
	public void setModulo(String modulo) {
		this.modulo = modulo;
	}
	
	

}
